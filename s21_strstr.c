char *s21_strstr(const char *haystack, const char *needle) {
  char *adress = S21_NULL;
  if (*needle == '\0') {
    adress = (char *)haystack;
  } else {
    while (*haystack != '\0' && adress == S21_NULL) {
      char *string = (char *)haystack;
      char *substring = (char *)needle;
      while (*string == *substring && adress == S21_NULL) {
        string++;
        substring++;
        if (*substring == '\0') {
          adress = (char *)haystack;
        }
      }
      haystack++;
    }
  }
  return adress;
}
